-- =============================================================================
-- Adding NOTIFICATIONS and ATTACHMENTS
-- =============================================================================

BEGIN
   FOR r IN (SELECT table_name FROM user_tables WHERE table_name IN ('NOTIFICATIONS', 'ATTACHMENTS')) LOOP
      EXECUTE IMMEDIATE 'DROP TABLE ' || r.table_name || ' CASCADE CONSTRAINTS';
   END LOOP;
END;
/

-- Create Table: NOTIFICATIONS
CREATE TABLE NOTIFICATIONS (
  NOTIFICATION_ID NUMBER             GENERATED BY DEFAULT AS IDENTITY,
  USER_ID         NUMBER             NOT NULL, -- Recipient
  TICKET_ID       NUMBER,                      
  TITLE           VARCHAR2(200 BYTE) NOT NULL,
  MESSAGE         CLOB               NOT NULL,
  IS_READ         NUMBER(1)          DEFAULT 0, -- 0 for Unread, 1 for Read
  CREATED_AT      DATE               DEFAULT SYSDATE,
  LINK_PAGE       VARCHAR2(255 BYTE),           
  PRIMARY KEY (NOTIFICATION_ID),
  CONSTRAINT FK_NOTIF_USER FOREIGN KEY (USER_ID) REFERENCES USERS (USER_ID),
  CONSTRAINT FK_NOTIF_TICKET FOREIGN KEY (TICKET_ID) REFERENCES TICKETS (TICKET_ID),
  CONSTRAINT CHK_NOTIF_ISREAD CHECK (IS_READ IN (0, 1))
) TABLESPACE BI_OJT;

-- Create Table: ATTACHMENTS
CREATE TABLE ATTACHMENTS (
  ATTACHMENT_ID      NUMBER              GENERATED BY DEFAULT AS IDENTITY,
  TICKET_ID          NUMBER              NOT NULL,
  ORIGINAL_FILE_NAME VARCHAR2(255 BYTE)  NOT NULL,
  SAVED_FILE_NAME    VARCHAR2(255 BYTE)  NOT NULL, 
  FILE_PATH          VARCHAR2(500 BYTE)  NOT NULL,
  FILE_SIZE          NUMBER,             
  FILE_TYPE          VARCHAR2(50 BYTE),  
  UPLOADED_BY        NUMBER              NOT NULL,
  UPLOADED_AT        DATE                DEFAULT SYSDATE,
  PRIMARY KEY (ATTACHMENT_ID),
  UNIQUE (SAVED_FILE_NAME),
  CONSTRAINT FK_ATTACH_TICKET FOREIGN KEY (TICKET_ID) REFERENCES TICKETS (TICKET_ID),
  CONSTRAINT FK_ATTACH_USER FOREIGN KEY (UPLOADED_BY) REFERENCES USERS (USER_ID)
) TABLESPACE BI_OJT;

COMMIT;